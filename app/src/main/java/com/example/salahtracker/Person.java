package com.example.salahtracker;

public class Person {
    public String name;
    public int id;

    public Person(String _name, int _id) {
        this.name=_name;
        this.id=_id;
    }
    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public int getId() {
        return this.id;
    }

    public void setId() {
        this.id = id;
    }



}
